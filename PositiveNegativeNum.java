package week1.day1.assginments;

public class PositiveNegativeNum {

	public static void main(String[] args) {
		int num=-40;
		if(num>0)
		{
			System.out.println(num+ " is positive");
		}
		else if(num<0)
			{
				System.out.println(num+ " is negative");
		}
			else
			{
				System.out.println(num+ " is zero");
			}
	}

}
